/** @jsx jsx */
import { jsx, useThemeUI, useColorMode } from 'theme-ui'
import { FontAwesomeIcon } from '@fortawesome/react-fontawesome'
import { faRobot } from '@fortawesome/free-solid-svg-icons'
import { Heading } from '@theme-ui/components'
import { Themed } from '@theme-ui/mdx'
import { TextBlock } from 'react-placeholder/lib/placeholders'
import isDarkMode from '../../../helpers/isDarkMode'

import 'react-placeholder/lib/reactPlaceholder.css'

/**
 * Skeleton placeholder for AI summary block. Matches the layout of AiSummary
 * so scroll-to-section doesn't drift. Uses configurable text rows (Steam ~3, Goodreads ~5).
 * Show More is shown as a disabled placeholder so height matches loaded state.
 */
const AiSummarySkeleton = ({ skeletonRows = 4 }) => {
  const [colorMode] = useColorMode()
  const { theme } = useThemeUI()
  const darkModeActive = isDarkMode(colorMode)
  const primary = theme?.colors?.primary ?? '#422EA3'
  const secondary = theme?.colors?.secondary ?? '#711E9B'
  const placeholderColor = darkModeActive ? '#3a3a4a' : '#efefef'

  return (
    <div
      aria-hidden
      sx={{
        variant: 'cards.aiSummary',
        mt: 4,
        mb: 4
      }}
    >
      <div
        sx={{
          display: 'flex',
          flexDirection: ['column', 'row'],
          alignItems: ['stretch', 'center'],
          gap: [3, 4],
          mb: 3
        }}
      >
        <div sx={{ flex: 1, minWidth: 0 }}>
          <div sx={{ display: 'flex', alignItems: 'center', mb: 3 }}>
            <FontAwesomeIcon
              icon={faRobot}
              sx={{ color: 'primary', fontSize: [2, 3], mr: 2, opacity: 0.6 }}
              aria-hidden
            />
            <Heading
              as='h3'
              sx={{
                fontSize: [3, 4],
                mb: 0,
                background: `linear-gradient(45deg, ${primary}, ${secondary})`,
                backgroundClip: 'text',
                WebkitBackgroundClip: 'text',
                WebkitTextFillColor: 'transparent'
              }}
            >
              AI Summary
            </Heading>
          </div>

          <div className='show-loading-animation' sx={{ '& p': { mb: 2, lineHeight: 1.6 } }}>
            <TextBlock color={placeholderColor} rows={skeletonRows} />
          </div>
        </div>

        {/* Disabled Show More placeholder — keeps layout height consistent with loaded state */}
        <div
          sx={{
            display: 'flex',
            justifyContent: ['center', 'flex-end'],
            flexShrink: 0,
            opacity: 0.5
          }}
        >
          <div
            sx={{
              height: '44px',
              minWidth: '140px',
              borderRadius: '8px',
              bg: placeholderColor
            }}
            aria-hidden
          />
        </div>
      </div>

      <Themed.p
        sx={{
          mb: 0,
          fontSize: [1, 2],
          color: 'textMuted',
          fontStyle: 'italic',
          display: 'flex',
          alignItems: 'center',
          opacity: 0.7
        }}
      >
        <FontAwesomeIcon icon={faRobot} sx={{ fontSize: 1, mr: 1 }} aria-hidden />
        Generated by Gemini (AI)
      </Themed.p>
    </div>
  )
}

export default AiSummarySkeleton
